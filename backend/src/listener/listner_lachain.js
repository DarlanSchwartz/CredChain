const runListenerLachain = async () => {


    const provider = new ethers.providers.WebSocketProvider('wss://lachain.linkpool.io/ws');
    const contractAddress = '0xB75314bA9Aae3b5B72F8d895d67f8079D214e0bD';
    const contract = new ethers.Contract(contractAddress, credChainAbi, provider);

    // Listen for the Deposit event specifically
    contract.on('Deposit', (from, token, amount, timestamp, event) => {
        console.log(`Deposit event detected: ${from} - ${token} - ${amount.toString()} - ${timestamp.toString()}`);
        try {
            const response = axios.post('http://localhost:5000/deposit-pvt', {
                from: from,
                token: token,
                amount: amount.toString(),
                timestamp: timestamp.toString()
            });

            console.log(`Dados enviados com sucesso! Resposta: ${response.data}`);
        } catch (error) {
            console.error('Erro ao enviar os dados:', error);
        }
    });

    provider.on("error", (error) => {
        console.error("Error:", error);
    });

    console.log('Monitoring Deposit events...');
}

export default runListenerLachain;